int n , k;
int a[1000005];
int F[1000005];
int x , y , l , r , mi , cnt;

void update(int i , int val){
    for(; i <= n ; i += i & (-i)) F[i] += val;
}

int get(int i){
    int res = 0;
    for(; i >= 1 ; i -= i & (-i)) res += F[i];
    return res;
}

