const long lm = 1e5+9;

long A[lm], B[lm], use, n, m;
pair<long, long> C[lm];
bool ck(const long &x){
    long p = 0, d=0;
    for (int i=1; i<=n; i++){
        if (A[i] >= x) continue;
        d += 1;
        p += 1;
        while (B[p] + A[i] < x){
            if (p > m) return false;
            p += 1;
        }
        C[d] = make_pair(i, p);
    }
    use = d;
    return true;
}
