bool check(int n , int m) {
    bool digit[10];
    memset(digit , false , sizeof(digit));
    while (n > 0) {
        if (n % 10 > m || digit[n % 10] == true || n % 10 == 0)return false;
        digit[n % 10] = true;
        n /= 10;
    }
    return true;
}

