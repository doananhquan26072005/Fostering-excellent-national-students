int n;
vi a[5];
int cnt[5];
int G[5][5];
bool dd[105];

struct Edge {
    int from, to , cost;
} canh[105];

int BFS(int s) {
    queue<int> S;
    S.push(s);
    int res = 0;
    set <int> tmp;
    while (!S.empty()) {
        int u = S.front();
        S.pop();
        tmp.insert(u);
        for (auto v : a[u]) {
            if (dd[v])continue;
            dd[v] = true;
            res += canh[v].cost;
            S.push(canh[v].from ^ canh[v].to ^ u);
        }
    }
    int dem = 0;
    for (auto it : tmp)if (cnt[it] % 2 == 1) dem += 1;
    if (dem == 4) {
        int t = oo;
        for (auto it : tmp)
            for (auto v : a[it]) t = min(t, canh[v].cost);
        return res - t;
    }
    return res;
}

