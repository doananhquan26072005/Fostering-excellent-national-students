void update(int fi, int se, int bit[]){
    while (fi <= total){
        bit[fi] += se;
        fi += fi & -fi;
    }
}

int get(int fi, int bit[]){
    int ans = 0;
    while (fi){
        ans += bit[fi];
        fi -= fi & -fi;
    }
    return ans;
}

